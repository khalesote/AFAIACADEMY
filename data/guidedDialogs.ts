const buildGrammarGuideStep = (dialog: GuidedDialog): DialogStep | null => {
  const guide = GRAMMAR_GUIDES[dialog.id];
  if (!guide) return null;

  const firstExampleEs = dialog.steps[0]?.userPrompt || '';
  const firstExampleAr = dialog.steps[0]?.userPromptAr || '';

  const botPrompt = [
    `Objetivo: ${guide.objectiveEs}`,
    `Regla clave: ${guide.ruleEs}`,
    `Pronombres personales útiles: ${guide.pronounsEs}`,
    `Estructura recomendada: ${guide.structureEs}`,
    `Consejo del asistente: ${guide.coachEs || 'Escucha, repite y responde despacio para fijar la gramática.'}`,
    firstExampleEs ? `Ejemplo guiado: ${firstExampleEs}` : '',
  ].filter(Boolean).join('\n');

  const botPromptAr = [
    `الهدف: ${guide.objectiveAr}`,
    `القاعدة الأساسية: ${guide.ruleAr}`,
    `ضمائر مفيدة: ${guide.pronounsAr}`,
    `البنية المقترحة: ${guide.structureAr}`,
    `نصيحة المساعد: ${guide.coachAr || 'استمع ثم كرّر وأجب ببطء لتثبيت القاعدة.'}`,
    firstExampleAr ? `المثال الموجّه: ${firstExampleAr}` : '',
  ].filter(Boolean).join('\n');

  return {
    id: `${dialog.id}-guia-organizada`,
    botPrompt,
    botPromptAr,
    userPrompt: 'Sí, entendido. Vamos a practicar.',
    userPromptAr: 'نعم، فهمت. هيا نتدرّب.',
    expected: ['si', 'entendido', 'practicar'],
    minMatches: 1,
    hint: 'Responde: "Sí, entendido. Vamos a practicar."',
    botPositive: 'Perfecto. Comenzamos el ejercicio guiado.',
  };
};

const withGrammarGuideSteps = (dialogs: GuidedDialog[]): GuidedDialog[] =>
  dialogs.map(dialog => {
    const guideStep = buildGrammarGuideStep(dialog);
    if (!guideStep) return dialog;
    if (dialog.steps.some(step => step.id === guideStep.id)) return dialog;
    return {
      ...dialog,
      steps: [guideStep, ...dialog.steps],
    };
  });

