const toArabicPronunciation = (text: string) => {
  let value = (text || '').toLowerCase();
  try {
    value = value.normalize('NFD').replace(/[\u0300-\u036f]/g, '');
  } catch {
    // no-op
  }

  value = value
    .replace(/\{\{name\}\}/g, '__NAME__')
    .replace(/\{\{country\}\}/g, '__COUNTRY__')
    .replace(/¿|¡/g, '')
    .replace(/ch/g, '§')
    .replace(/ll/g, '¤')
    .replace(/rr/g, '¥')
    .replace(/qu/g, 'k')
    .replace(/gue/g, 'ge')
    .replace(/gui/g, 'gi')
    .replace(/ge/g, 'xe')
    .replace(/gi/g, 'xi')
    .replace(/j/g, 'x')
    .replace(/ñ/g, 'ny')
    .replace(/ce/g, 'se')
    .replace(/ci/g, 'si')
    .replace(/z/g, 's')
    .replace(/v/g, 'b')
    .replace(/h/g, '');

  const map: Record<string, string> = {
    a: 'ا',
    b: 'ب',
    c: 'ك',
    d: 'د',
    e: 'ي',
    f: 'ف',
    g: 'غ',
    i: 'ي',
    k: 'ك',
    l: 'ل',
    m: 'م',
    n: 'ن',
    o: 'و',
    p: 'ب',
    q: 'ك',
    r: 'ر',
    s: 'س',
    t: 'ت',
    u: 'و',
    w: 'و',
    x: 'خ',
    y: 'ي',
  };

  let out = '';
  for (let i = 0; i < value.length; i += 1) {
    const ch = value.charAt(i);
    if (ch === '§') { out += 'تش'; continue; }
    if (ch === '¤') { out += 'ي'; continue; }
    if (ch === '¥') { out += 'ر'; continue; }
    if (ch === ' ') { out += ' '; continue; }
    if (ch === ',') { out += '،'; continue; }
    if (ch === '?') { out += '؟'; continue; }
    if (ch === '_' || ch === '{' || ch === '}') { out += ch; continue; }
    if (ch === ';' || ch === ':' || ch === '.' || ch === '!' || ch === '(' || ch === ')' || ch === '-' || ch === '/' || ch === '"') {
      out += ch;
      continue;
    }
    out += map[ch] || ch;
  }

  return out
    .replace(/__name__/g, '{{name}}')
    .replace(/__country__/g, '{{country}}')
    .replace(/\s+/g, ' ')
    .trim();
};

